		<div class="accordion accordion-style1">
		 	<h3 class="accordion-title"><a href="#">Is IAHV Australia an Australian Charity?</a></h3>
		 	<div><p>Yes, we are. We work exclusively in Australia for the benefit of eligible Australians.
			</p>
		</div>
		<h3 class="accordion-title"><a href="#">What is IAHV Australia’s relationship to its parent organisation?</a></h3>
		 	<div>
		<p>Our parent organisation is based in Switzerland and has independent arms across the world. IAHV Australia is the Australian arm of the international IAHV body and is legally and financially independent from its international parent.
           In Australia we function broadly according to the global IAHV charter, but interpret the charter to suit the specific needs of Australian society.
		</p>
		</div>
		<h3 class="accordion-title"><a href="#">What is IAHV Australia’s Charity Status?</a></h3>
		 <div><p>We are a Tax Concession charity with ITEC status. Thus we do not pay income tax, are not required to charge GST, but may claim GST credits.
     		 We do not yet have Deductible Gift Recipient Endorsement but will apply soon.
			</p><p></p></div>
			<h3 class="accordion-title"><a href="#">What happens to the money?</a></h3>
		 	<div>
			<p>IAHV Australia raises money only in Australia. Funds remain in Australia to support work in Australia.  We do not send money overseas. We honour donor instructions as to which projects their money supports.
		</p>
		</div>
			<h3 class="accordion-title"><a href="#">How much is spent on overheads?</a></h3>
		 	<div>
			<p>The international IAHV standard aims for overheads of 3% of gross income and this is the goal of IAHV Australia.
		</p>
		</div>
		<h3 class="accordion-title"><a href="#">How can one possibly achieve overheads of only 3%?</a></h3>
		 	<div>
			<p>Like other global IAHV arms, IAHV Australia is a 100% volunteer driven organisation. Managers, organisers and facilitators donate their personal time, resources and professional skills to the organisation and its projects. We do not run fleet cars or maintain large offices. Except where grant conditions specify, IAHV Australia does not employ paid staff. Our overheads are consequently far lower than most charities. Where possible, unavoidable project related expenses are shared with project partners, sponsored or donated.
		</p>
		</div>
			<h3 class="accordion-title"><a href="#">Do you carry insurance?</a></h3>
		 	<div>
			<p>Yes. IAHV Australia is fully insured – public liability, product liability and professional indemnity.
		</p>
		</div>
			<h3 class="accordion-title"><a href="#">Where are volunteers based?</a></h3>
		 	<div>
   			<p>We have access to large numbers of national volunteers who are experienced organisers or highly trained facilitators. Volunteers have a proven track record and long term commitment to the organisation and its values. IAHV Australia draws on hundreds of skilled people in WA, ACT, NSW, QLD and VIC.
   			</p>
		</div>
		
		<h3 class="accordion-title"><a href="#">What are facilitator training standards?</a></h3>
		 	<div>
   			<p>IAHV Australia facilitators are committed to the organisation’s work and values and continue lifelong development training at national and international locations. Facilitators are selected by an international panel and specialise in interventions for different age groups, health or community conditions.
            Qualifying as a facilitator takes some years. In addition to intervention knowledge, it requires a permanently healthy personal lifestyle including abstinence from alcohol, smoking and recreational drugs, a vegetarian diet and moderate exercise.
   			</p>
		</div>
